exports.getTourStats = async (req, res) => {
  try {
    const stats = await Tour.aggregate([
      {
        $match: { ratingsAverage: { $gte: 4.5 } },
      },
      {
        $group: {
          _id: { $toUpper: "$difficulty" },
          numTours: { $sum: 1 },
          numRatings: { $sum: "$ratingsQuantity" },
          avgRating: { $avg: "$ratingsAverage" },
          avgPrice: { $avg: "$price" },
          minPrice: { $min: "$price" },
          maxPrice: { $max: "$price" },
        },
      },
      {
        $sort: { avgPrice: 1 },
      },
      // {
      //   $match: { _id: { $ne: 'EASY' } } // '$ne' is not equal
      // } //repeating stages
    ]);

    res.status(200).json({
      status: "success",
      data: {
        stats,
      },
    });
  } catch (err) {
    res.status(404).json({
      status: "fail",
      message: err,
    });
  }
};

//each of the tours, remember, has an array of these start dates. these dates is what we actually need as a starting point to create this function or to create this aggregation pipeline. Because remember, we want to count how many tours there are for each of the months in a given year. So if you want to add all of this together the easiest way would basically be to have one tour for each of these dates here, right? And we can actually do that using the aggregation pipeline. There is a stage for doing exactly that. And that is called 'unwind'.

exports.getMonthlyPlan = async (req, res) => {
  try {
    const year = req.params.year * 1; // 2021. Taking year from the url parameter.

    const plan = await Tour.aggregate([
      {
        $unwind: "$startDates", //unwind deconstructs an array field from the info documents and then output one document for each element of the array. And the field with the array that we want to unwind is 'startDates'.
      },
    ]);

    res.status(200).json({
      status: "success",
      data: {
        plan,
      },
    });
  } catch (err) {
    res.status(404).json({
      status: "fail",
      message: err,
    });
  }
};

